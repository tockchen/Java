public class Calculator {
    /**
     *
     * @param a 整数
     * @param b 整数
     * @return 两数之和
     */
    public int add (int a,int b) {
        return a + b;
    }

    /**
     *
     * @param a 整数
     * @param b 整数
     * @return 两数之差
     */
    public int sub (int a,int b) {
        return a - b;
    }
}
